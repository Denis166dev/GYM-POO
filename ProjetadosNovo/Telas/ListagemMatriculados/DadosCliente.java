    private void carregarDadosCliente() {
        try {
            Aluno aluno = alunoDAO.buscarPorMatricula(matricula);
            if (aluno != null) {
                // Preenche os campos com os dados do aluno
                lblnomecliente.setText(aluno.getNome());
                lblemailcliente.setText(aluno.getEmail());
                lbltelefonenum.setText(aluno.getNumerocel());
                //Formata a data antes de setar no label
                if (aluno.getNascimento() != null) {
                    lblnascimento.setText(aluno.getNascimento().format(DateTimeFormatter.ofPattern("dd/MM/yyyy")));
                }
                if (aluno.getHorarioCadastro() != null) {
                    lblhoracadastro.setText(aluno.getHorarioCadastro().format(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss")));
                }
                lblplanotipo.setText(aluno.getPlano());
                lblsituacaotipo.setText("Ativa"); //  Lógica para determinar a situação

            } else {
                JOptionPane.showMessageDialog(this, "Aluno não encontrado.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados do cliente: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

